"""
Prepare v12 training data: balance QA with verbatim recall and comparison.

v11 analysis: definition/methodology/specific_fact solved, but verbatim_recall
regressed (0.600→0.133) and comparison unstable (0.167-0.667).

Root cause: QA-heavy training crowds out verbatim recall. The model learns
to answer questions well but forgets to reproduce exact paper text.

v12 strategy:
  - All v11 QA (115 unique) + new verbatim + comparison QA (~15 more)
  - QA × 8 copies (reduced from 10 to leave room for paraphrases)
  - Paraphrases × 2 (double the paraphrase count for verbatim backing)
  - Same LR=5e-5, LoRA=64, 4 epochs

Output: data/paper_cpt/v12_augmented.jsonl
"""

import json
import random

PAPER_TITLE = "Reward Is Enough: LLMs Are In-Context Reinforcement Learners"
AUTHORS = "Kefan Song, Amir Moeini, Peng Wang, Lei Gong, Rohan Chandra, Shangtong Zhang, and Yanjun Qi"


def s(user, assistant):
    return {"messages": [
        {"role": "user", "content": user},
        {"role": "assistant", "content": assistant},
    ]}


