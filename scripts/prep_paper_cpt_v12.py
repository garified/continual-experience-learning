def main():
    random.seed(42)

    # Load all unique QA from v11
    qa_v11 = []
    seen = set()
    with open("data/paper_cpt/v11_augmented.jsonl") as f:
        for line in f:
            d = json.loads(line)
            q = d["messages"][0]["content"]
            if q != "." and q not in seen:
                seen.add(q)
                qa_v11.append(d)
    print(f"v11 QA pairs (unique): {len(qa_v11)}")

    # Generate v12-specific QA
    v12_qa = generate_v12_qa()
    print(f"v12 new QA pairs: {len(v12_qa)}")

    # Combine all unique QA
    all_qa = qa_v11.copy()
    for q in v12_qa:
        user_msg = q["messages"][0]["content"]
        if user_msg not in seen:
            seen.add(user_msg)
            all_qa.append(q)
        else:
            # Update existing with v12 version
            for i, existing in enumerate(all_qa):
                if existing["messages"][0]["content"] == user_msg:
                    all_qa[i] = q
                    break
    print(f"Total unique QA: {len(all_qa)}")

    # QA × 8 copies (reduced from 10 to balance with paraphrases)
    qa_repeated = all_qa * 8
    print(f"QA × 8: {len(qa_repeated)}")

    # Load paraphrases × 2 (doubled to reinforce verbatim recall)
    paraphrases = []
    with open("data/paper_cpt/v6_augmented.jsonl") as f:
        for line in f:
            paraphrases.append(json.loads(line))
    paraphrases_x2 = paraphrases * 2
    print(f"Paraphrases × 2: {len(paraphrases_x2)}")

    # Combine
    all_samples = qa_repeated + paraphrases_x2
    print(f"Total: {len(all_samples)}")

    # Shuffle
    random.seed(42)
    random.shuffle(all_samples)

    # Write
    output = "data/paper_cpt/v12_augmented.jsonl"
    with open(output, "w") as f:
        for sample in all_samples:
            f.write(json.dumps(sample, ensure_ascii=False) + "\n")
    print(f"Written to {output}")
    with open(output) as f:
        print(f"Verified: {sum(1 for _ in f)} lines")


if __name__ == "__main__":
    main()
